

//# 3. comparison operators

//! A comparison operator compares its operand and return a logical value based  on whether the comparison is true or false

var a = 30;
var b = 10;

//$ Equal (==)
console.log(a === b);

//$ Not equal (!=) 
console.log( a != b);

//$ Greater then (>)
console.log(a > b);


//$ Greater then and equal to (>=)
console.log( a >= b);

//$ less then (<)
console.log(a < b);

//$ less then and equal to (<=)
console.log( a <= b);

